def build_keyboard(choices: list[tuple[str, str]]) -> InlineKeyboardMarkup:
    buttons = [
        [InlineKeyboardButton(text=label, callback_data=node_id)]
        for label, node_id in choices
    ]
    return InlineKeyboardMarkup(buttons)


async def render_node(
    node_id: str,
    update: Update,
    context: ContextTypes.DEFAULT_TYPE,
    edit: bool = False,
) -> None:
    node = NODES.get(node_id)
    if not node:
        return

    keyboard = build_keyboard(node["choices"])

    if edit and update.callback_query:  # noqa
        # При переходе на START через кнопку — отправляем новым сообщением с фото,
        # редактировать фото-сообщение в текст нельзя
        if node_id == "START":
            chat_id = update.callback_query.message.chat_id
            await _send_start_with_photo(context, chat_id, node["text"], keyboard)
        else:
            await update.callback_query.edit_message_text(
                text=node["text"],
                parse_mode="Markdown",
                reply_markup=keyboard,
            )
    else:
        chat_id = (
            update.effective_chat.id
            if update.effective_chat
            else update.callback_query.message.chat_id
        )
        if node_id == "START":
            await _send_start_with_photo(context, chat_id, node["text"], keyboard)
        else:
            await context.bot.send_message(
                chat_id=chat_id,
                text=node["text"],
                parse_mode="Markdown",
                reply_markup=keyboard,
            )


async def _send_start_with_photo(context, chat_id, text, keyboard):
    """Отправляет стартовую сцену с картинкой."""
    if START_IMAGE_PATH.exists():
        with open(START_IMAGE_PATH, "rb") as photo:
            await context.bot.send_photo(
                chat_id=chat_id,
                photo=InputFile(photo, filename="start_image.jpg"),
                caption=text,
                parse_mode="Markdown",
                reply_markup=keyboard,
            )
    else:
        # Если файл не найден — отправляем просто текст
        await context.bot.send_message(
            chat_id=chat_id,
            text=text,
            parse_mode="Markdown",
            reply_markup=keyboard,
        )


