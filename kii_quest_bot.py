def build_keyboard(choices: list[tuple[str, str]]) -> InlineKeyboardMarkup:
    buttons = [
        [InlineKeyboardButton(text=label, callback_data=node_id)]
        for label, node_id in choices
    ]
    return InlineKeyboardMarkup(buttons)


async def render_node(
    node_id: str,
    update: Update,
    context: ContextTypes.DEFAULT_TYPE,
    edit: bool = False,
) -> None:
    node = NODES.get(node_id)
    if not node:
        return

    keyboard = build_keyboard(node["choices"])

    if edit and update.callback_query:
        await update.callback_query.edit_message_text(
            text=node["text"],
            parse_mode="Markdown",
            reply_markup=keyboard,
        )
    else:
        chat_id = (
            update.effective_chat.id
            if update.effective_chat
            else update.callback_query.message.chat_id
        )
        await context.bot.send_message(
            chat_id=chat_id,
            text=node["text"],
            parse_mode="Markdown",
            reply_markup=keyboard,
        )


